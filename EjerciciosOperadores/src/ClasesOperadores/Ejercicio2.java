package ClasesOperadores;

public class Ejercicio2 {

	public static void main(String[] args) {
		int numero1=9;
		int numero2=7;
		int suma=numero1+numero2;
		int resta=numero1-numero2;
		int multiplicacion=numero1*numero2;
		double division=(double)numero1/numero2;
		
		System.out.println("El resulatado de la suma de "+numero1 +" más "+ numero2+" es: "+suma);
		System.out.println("El resulatado de la resta de "+numero1 +" menos "+ numero2+" es: "+resta);
		System.out.println("El resulatado de la multiplicación de "+numero1 +" por "+ numero2+" es: "+multiplicacion);
		System.out.println("El resulatado de la división de "+numero1 +" dividido por "+ numero2+" es: "+division);
	
	}

}
